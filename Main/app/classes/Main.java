package classes;

public class Main {
    int x;
    int y;

    // public, which can only be
    // accessed by objects:
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
