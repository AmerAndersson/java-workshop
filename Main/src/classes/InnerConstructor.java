package classes;

/*
 * Create a class attribute
 * Create a class constructor for the InnerConstructor class
 * Set the initial value for the class attribute modelYear and modelName
 */
public class InnerConstructor {

    public int modelYear;
    public String modelName;

    // Constructor Parameters
    public InnerConstructor(int year, String name) {
        modelYear = year;
        modelName = name;

    }
}
